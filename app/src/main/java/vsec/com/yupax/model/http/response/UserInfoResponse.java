public class UserInfoResponse {

    private String rating;
    private String ratingCode;
    private String groupRating;
    private String recensyName;
    private String frequencyName;
    private String monetaryName;
    private String token;
    private String username;
    private String firstName;
    private String lastName;
    private String email;
    private String mobile;
    private String status;

    public String getRating() {
        return rating;
    }

    public void setRating(String rating) {
        this.rating = rating;
    }

    public String getRatingCode() {
        return ratingCode;
    }

    public void setRatingCode(String ratingCode) {
        this.ratingCode = ratingCode;
    }

    public String getGroupRating() {
        return groupRating;
    }

    public void setGroupRating(String groupRating) {
        this.groupRating = groupRating;
    }

    public String getRecensyName() {
        return recensyName;
    }

    public void setRecensyName(String recensyName) {
        this.recensyName = recensyName;
    }

    public String getFrequencyName() {
        return frequencyName;
    }

    public void setFrequencyName(String frequencyName) {
        this.frequencyName = frequencyName;
    }

    public String getMonetaryName() {
        return monetaryName;
    }

    public void setMonetaryName(String monetaryName) {
        this.monetaryName = monetaryName;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
